    private void btn_xoaBangThongKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaBangThongKeActionPerformed

        // Kiểm tra có muốn xóa hay không 
        if (ThongBao.ThongBaoXacNhan(giaoDienChinh, "Bạn có muốn xóa bảng thống kê không?", "Hỏi") == JOptionPane.NO_OPTION) {
            return; // nếu không muốn -> kết thúc sự kiện
        }
        try {
            // B1: Tạo kết nối
            Connection con = DatabaseHelper.openConnection();
            // B2: Tạo đối tượng pst + lệnh sql
            String sql = "DELETE FROM HoaDon ";
            PreparedStatement pst = con.prepareStatement(sql);

            // B3: Thực thi sql
            int ketQua = pst.executeUpdate();
            // B4: Xử lý dữ liệu
            System.out.println("Ban da thuc thi " + sql);
            System.out.println("Có " + ketQua + " ket qua bi thay doi!!");
            // B5: Đóng csdl
            con.close();
            //DatLaiGiaTriSTT();
            ThongBao.ThongBao(giaoDienChinh, "Xóa bảng thống kê thành công!", "Thông báo");
            TaiDuLieuLenBang_ThongKe();
            lbl_doanhThu.setText("0");
            
        } catch (Exception e) {
            e.printStackTrace();
            ThongBao.ThongBao(giaoDienChinh, "Xóa bảng thống kê thất bại", "Thông báo");
        }
    }//GEN-LAST:event_btn_xoaBangThongKeActionPerformed

