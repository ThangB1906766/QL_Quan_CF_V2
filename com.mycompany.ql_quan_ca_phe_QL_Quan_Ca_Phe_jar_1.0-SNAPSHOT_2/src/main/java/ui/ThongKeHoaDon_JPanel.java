    public void TinhDoanhThu() {
        try {
            // B1: Tạo kết nối
            Connection con = DatabaseHelper.openConnection();
            // B2: Tạo đối tượng pst + lệnh sql
            String sql = "SELECT SUM(thanhTien) as doanhThu FROM HoaDon ";
            PreparedStatement pst = con.prepareStatement(sql);

            // B3: Thực thi sql
            ResultSet rs = pst.executeQuery();
            // B4: Xử lý dữ liệu
            System.out.println("Ban da thuc thi " + sql);

            while (rs.next()) {

                int doanhThu = (int)rs.getFloat("doanhThu");
                lbl_doanhThu.setText(String.valueOf(doanhThu));

            }
            // B5: Đóng csdl
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void TaiDuLieuLenBang_ThongKe() {
        try {

            HoaDonDAO hoaDonDAO = new HoaDonDAO();
            List<HoaDon> list = hoaDonDAO.selectAll(); // Trả về all nhân viên trong csdl
            tblmodel.setRowCount(0); // Xóa dữ liệu hiện đang trên bảng
            for (HoaDon hoaDon : list) {
                tblmodel.addRow(new Object[]{
                    hoaDon.getMaHoaDon(), hoaDon.getTenDangNhap(), hoaDon.getTenBan(), hoaDon.getThanhTien(),
                    hoaDon.getNgayLap()
                });
            }
            tblmodel.fireTableDataChanged(); // Cập nhật lại hiển thị dữ liệu trong bảng
        } catch (Exception e) {
            e.printStackTrace();
            ThongBao.ThongBaoLoi(giaoDienChinh, e.getMessage(), "Lỗi");
        }
    }

